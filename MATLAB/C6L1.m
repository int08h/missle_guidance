clear
n=0;
QD=0.;
XNT=32.2;
XNP=4.;
T1=.0667;
T2=.133;
T3=.2;
T4=.267;
T5=.333;
W=10;
Z=.7;
TF=10;
VC=4000;
T=0.;
S=0;
TP=T+.00001;
X1=0;
X2=0;
X3=1;
X4=0;
X5=0;
X6=0;
X7=0;
X8=0;
X9=0;
X10=0;
H=.01; 
while TP<=(TF-1e-5)
	S=S+H;
	X1OLD=X1;
	X2OLD=X2;
	X3OLD=X3;
	X4OLD=X4;
	X5OLD=X5;
	X6OLD=X6;
	X7OLD=X7;
	X8OLD=X8;
	X9OLD=X9;
	X10OLD=X10;
	STEP=1;
	FLAG=0;
	while STEP<=1
      		if FLAG==1
         		STEP=2;
				X1=X1+H*X1D;
				X2=X2+H*X2D;
				X3=X3+H*X3D;
				X4=X4+H*X4D;
				X5=X5+H*X5D;
				X6=X6+H*X6D;
				X7=X7+H*X7D; 
				X8=X8+H*X8D; 
				X9=X9+H*X9D; 
				X10=X10+H*X10D; 
				TP=TP+H;
			end
			X1D=X2;
			X2D=X3;
			TGO=TP+.00001;
			X3D=(X4+X5/T2)/(VC*TGO*T1);
			X4D=-(X4+X5/T2)/T1;
			X5D=-X5/T2+XNP*VC*X6/T3;
			X6D=-X6/T3+QD*W*W*X9+(1.-QD)*X7/T4;
			X7D=-X7/T4+X8/T5;
			X8D=-X8/T5-X2;
			X9D=X10-2.*Z*W*X9;
			X10D=-W*W*X9-X2;
			FLAG=1;
   	end
   	FLAG=0; 
	X1=(X1OLD+X1)/2+.5*H*X1D; 
	X2=(X2OLD+X2)/2+.5*H*X2D;
	X3=(X3OLD+X3)/2+.5*H*X3D;
	X4=(X4OLD+X4)/2+.5*H*X4D;
	X5=(X5OLD+X5)/2+.5*H*X5D;
	X6=(X6OLD+X6)/2+.5*H*X6D;
	X7=(X7OLD+X7)/2+.5*H*X7D;
	X8=(X8OLD+X8)/2+.5*H*X8D;
	X9=(X9OLD+X9)/2+.5*H*X9D;
	X10=(X10OLD+X10)/2+.5*H*X10D; 
	if S>=.0999
		S=0;
		XMNT=XNT*X1;
		n=n+1;
      	ArrayTP(n)=TP;
      	ArrayXMNT(n)=XNT*X1; 
	end
end
figure
plot(ArrayTP,ArrayXMNT),grid
xlabel('Flight Time (Sec)')
ylabel('Target Maneuver Miss (Ft)')
clc
output=[ArrayTP',ArrayXMNT'];
save datfil.txt output -ascii
disp 'simulation finished'