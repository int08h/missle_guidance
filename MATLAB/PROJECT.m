function[YB,YDB,YTDDB,YTDDDB]=PROJECT(TP,TS,YPH,YDPH,...
			YTDDPH,YTDDDPH,HP,XNLP,WPH)
T=0.;
Y=YPH;
YD=YDPH;
YTDD=YTDDPH;
YTDDD=YTDDDPH;
W=WPH;
XNL=XNLP;
H=HP;
while T<=(TS-.0001)
	YTDDDD=-W*W*YTDD;
	YTDDD=YTDDD+H*YTDDDD;
	YTDD=YTDD+H*YTDDD;
	YDD=YTDD-XNL;
	YD=YD+H*YDD;
	Y=Y+H*YD;
	T=T+H;
end
YB=Y;
YDB=YD;
YTDDB=YTDD;
YTDDDB=YTDDD;
