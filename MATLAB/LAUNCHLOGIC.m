function [VM1,VM2,VM3,TF]=LAUNCHLOGIC(RM1,RM2,RM3,RT1,RT2,RT3,...
									  VT1,VT2,VT3,VM)
for TF=.1:.1:10,
		RTM1=RT1-RM1;
		RTM2=RT2-RM2;
		RTM3=RT3-RM3;
		RT1F=RT1+VT1*TF;
		RT2F=RT2+VT2*TF;
		RT3F=RT3+VT3*TF;
		THET=asin((RT2F-RM2)/(VM*TF));
		PSI=atan2(RT3F-RM3,RT1F-RM1);
		VM1=VM*cos(THET)*cos(PSI);
		VM2=VM*sin(THET);
		VM3=VM*cos(THET)*sin(PSI);
		RM1F=RM1+VM1*TF;
		RM2F=RM2+VM2*TF;
		RM3F=RM3+VM3*TF;
		RTM1F=RT1F-RM1F;
		RTM2F=RT2F-RM2F;
		RTM3F=RT3F-RM3F;
		RTMF=sqrt(RTM1F^2+RTM2F^2+RTM3F^2);
		VTM1=VT1-VM1;
		VTM2=VT2-VM2;
		VTM3=VT3-VM3;
		VC=-(RTM1F*VTM1+RTM2F*VTM2+RTM3F*VTM3)/RTMF;
		if VC<0
			break
		end
end
